package basic;

public class AssignmentOperators {

	public static void main(String[] args) {

		int a = 10;
		int b = 5;

		int c = a;
		System.out.println(c);

		int d = b;
		System.out.println(d);

		int e = 20;
		e++;
		System.out.println(e);

		e--;
		System.out.println(e);

		e += 5;
		System.out.println(e);

		e -= 5;
		System.out.println(e);

		e *= 5;
		System.out.println(e);

		e /= 5;
		System.out.println(e);

		e %= 5;
		System.out.println(e);
	}

}
